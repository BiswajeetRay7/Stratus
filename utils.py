# Loader animation for scanning
class Loader:
    def __init__(self, text="Scanning"):
        self.text = text
        self.done = False

    def start(self):
        threading.Thread(target=self._animate, daemon=True).start()

    def _animate(self):
        for c in itertools.cycle(['|', '/', '-', '\\']):
            if self.done:
                break
            sys.stdout.write(f'\r{self.text} {c}')
            sys.stdout.flush()
            time.sleep(0.1)
        sys.stdout.write('\r‚úÖ Scan completed!    \n')

    def stop(self):
        self.done = True
